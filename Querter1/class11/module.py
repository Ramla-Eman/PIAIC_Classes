def add(num1, num2):
    return num1 + num2


def substraction(num1, num2):
    return num1 - num2


def multiply(num1, num2):
    return num1 * num2


def divide(num1, num2):
    return num1 / num2


def exponent(num1, num2):
    return num1 ** num2


def floor_dividion(num1, num2):
    return num1 // num2